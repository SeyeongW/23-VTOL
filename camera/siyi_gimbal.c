// CRC16 계산 함수
uint16_t calculate_crc16(unsigned char *ptr, int len) {
    uint16_t crc = 0;
    while (len-- > 0) {
        crc = (crc << 8) ^ crc16_tab[((crc >> 8) ^ *ptr++) & 0xff];
    }
    return crc;
}

// 짐벌 속도 명령 전송
void send_gimbal_speed(int sockfd, struct sockaddr_in *addr, int yaw_speed, int pitch_speed) {
    // 속도 범위 제한 (-100 ~ 100)
    if (yaw_speed > 100) yaw_speed = 100;
    if (yaw_speed < -100) yaw_speed = -100;
    if (pitch_speed > 100) pitch_speed = 100;
    if (pitch_speed < -100) pitch_speed = -100;

    unsigned char packet[12]; // 12 bytes total size

    // Header
    packet[0] = 0x55; // STX
    packet[1] = 0x66; // STX
    packet[2] = 0x01; // CTRL (Need ACK)
    
    // Data Length (2 bytes, Little Endian) -> 0x02 0x00
    packet[3] = 0x02; 
    packet[4] = 0x00; 
    
    // Sequence (2 bytes, Little Endian) -> 0x00 0x00 (Fixed for simplicity)
    packet[5] = 0x00;
    packet[6] = 0x00;
    
    // Command ID -> 0x07 (GIMBAL_SPEED)
    packet[7] = 0x07;
    
    // Data (Payload) -> Yaw(int8) + Pitch(int8)
    packet[8] = (char)yaw_speed;
    packet[9] = (char)pitch_speed;
    
    // CRC16 Calculation (from index 0 to 9)
    uint16_t crc = calculate_crc16(packet, 10);
    
    // CRC (2 bytes, Little Endian)
    packet[10] = (unsigned char)(crc & 0xFF); // Low byte
    packet[11] = (unsigned char)((crc >> 8) & 0xFF); // High byte

    // Send UDP packet
    sendto(sockfd, packet, 12, 0, (struct sockaddr *)addr, sizeof(*addr));
    
    printf("[TX] Yaw: %d, Pitch: %d | Hex: ", yaw_speed, pitch_speed);
    for(int i=0; i<12; i++) printf("%02X ", packet[i]);
    printf("\n");
}

int main(int argc, char *argv[]) {
    // 인자로 4개의 좌표값(ymin, xmin, ymax, xmax)을 받습니다.
    if (argc != 5) {
        printf("Usage: %s <ymin> <xmin> <ymax> <xmax>\n", argv[0]);
        // 인자 없으면 정지 명령 보내고 종료
        // (실제 사용 시에는 소켓 열고 0,0 보내는 로직 필요)
        return -1;
    }

    // 좌표 파싱 (0.0 ~ 1.0)
    float ymin = atof(argv[1]);
    float xmin = atof(argv[2]);
    float ymax = atof(argv[3]);
    float xmax = atof(argv[4]);

    // 중심점 계산
    float cx = (xmin + xmax) / 2.0f;
    float cy = (ymin + ymax) / 2.0f;

    // 에러 계산 (중앙 0.5 기준)
    float err_x = cx - 0.5f;
    float err_y = cy - 0.5f;

    // Deadzone 체크
    if (fabs(err_x) < DEADZONE) err_x = 0;
    if (fabs(err_y) < DEADZONE) err_y = 0;

    // 속도 명령 계산 (P 제어)
    int yaw_cmd = (int)(err_x * 100 * KP_X);
    int pitch_cmd = (int)(-err_y * 100 * KP_Y); // Pitch 방향 반전 필요시 부호 변경

    // 소켓 초기화
    int sockfd;
    struct sockaddr_in servaddr;

    if ((sockfd = socket(AF_INET, SOCK_DGRAM, 0)) < 0) {
        perror("socket creation failed");
        return -1;
    }

    memset(&servaddr, 0, sizeof(servaddr));
    servaddr.sin_family = AF_INET;
    servaddr.sin_port = htons(SERVER_PORT);
    servaddr.sin_addr.s_addr = inet_addr(SERVER_IP);

    // 명령 전송
    send_gimbal_speed(sockfd, &servaddr, yaw_cmd, pitch_cmd);

    close(sockfd);
    return 0;
}